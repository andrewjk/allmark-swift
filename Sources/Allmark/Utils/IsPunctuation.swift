import Foundation

func isPunctuation(code: UInt32) -> Bool {
	switch code {
	case 0x21, 0x22, 0x23, 0x24, 0x25, 0x26, 0x27, 0x28, 0x29, 0x2A, 0x2B, 0x2C, 0x2D, 0x2E, 0x2F,
	     0x3A, 0x3B, 0x3C, 0x3D, 0x3E, 0x3F, 0x40,
	     0x5B, 0x5C, 0x5D, 0x5E, 0x5F, 0x60,
	     0x7B, 0x7C, 0x7D, 0x7E:
		return true
	default:
		return false
	}
}
